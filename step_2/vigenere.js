function desencriptar(texto){
  var texto_descifrado = "";
  var j = 0;
  for(var i = 0; i < texto.length; i++){
    var index = vigenere[0].indexOf(texto[i]);
    if(index != -1){
      // FILA DE LA CLAVE
      var clave_index  = vigenere[0].indexOf(clave[j % 5]);
      // POSICION DE LA LETRA CIFRADA EN LA FILA DE LA CLAVE
      var texto_index  = vigenere[clave_index].indexOf(texto[i]);
      // LETRA ORIGINAL
      var letra = vigenere[0][texto_index];
      // AÃ‘ADIMOS LA LETRA
      texto_descifrado += letra;
      j++;
    }else if(texto[i] == " "){
      texto_descifrado += " ";
    }
  }

  console.log(texto_descifrado);
}


